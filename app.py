import pandas as pd
import streamlit as st
import duckdb
import io
#----------------------------------------------------------------------------------------
#----------------------------------------------------------------------------------------
csv = '''
beverage, price
orange juice, 2.5
Expresso, 3
Tea, 4
'''
beverages = pd.read_csv(io.StringIO(csv))
#----------------------------------------------------------------------------------------
#----------------------------------------------------------------------------------------
csv2 = '''
food_item, food_price
cookie , 2.5
Chocloatine, 3
muffin, 4
'''
food_items = pd.read_csv(io.StringIO(csv2))
#----------------------------------------------------------------------------------------
#----------------------------------------------------------------------------------------
answer = '''
SELECT *
FROM beverages
CROSS JOIN food_items
'''
#----------------------------------------------------------------------------------------
#----------------------------------------------------------------------------------------
solution = duckdb.sql(answer).df()
